class TransportPropertiesGenerator:
    """生成 transportProperties 文件"""

    TURBULENCE_MODELS = ["laminar", "kEpsilon", "kOmega", "SpalartAllmaras"]

    @classmethod
    def generate(cls, case_path: Path, turbulence: str = "kEpsilon", **kwargs) -> Path:
        defaults = {
            "simulationType": turbulence,
            "laminar": {"viscosity": "nu [ 0 2 -1 0 0 0 0 ] 1e-05;"},
            "RAS": {
                "RASModel": turbulence,
                "turbulence": "on",
                "printCoeffs": True,
            },
        }
        defaults.update(kwargs)
        path = case_path / "constant" / "transportProperties"
        OFDictWriter.write(path, defaults, header="Transport properties")
        return path


class ConfigGenerator:
    """
    统一配置生成器。
    根据求解器类型生成完整的 OpenFOAM case 配置文件。
    """

    def __init__(self, case_path: Path):
        self.case_path = Path(case_path)

    def generate_all(self, solver: str = "simpleFoam", turbulence: str = "kEpsilon",
                     start_time: float = 0, end_time: float = 1000,
                     delta_t: float = 1, **kwargs) -> List[Path]:
        """
        生成所有必要的配置文件。

        Returns:
            生成的文件路径列表
        """
        paths = []
        paths.append(ControlDictGenerator.generate(self.case_path, solver,
                                                  startTime=start_time, endTime=end_time, deltaT=delta_t, **kwargs))
        paths.append(FvSchemesGenerator.generate(self.case_path))
        paths.append(FvSolutionGenerator.generate(self.case_path))
        paths.append(TransportPropertiesGenerator.generate(self.case_path, turbulence))
        logger.info(f"配置文件已生成: {len(paths)} 个文件")
        return paths

    def generate_from_dict(self, configs: Dict[str, Dict[str, Any]]) -> List[Path]:
        """
        从配置字典生成配置文件。

        Args:
            configs: {"controlDict": {...}, "fvSchemes": {...}, ...}
        """
        paths = []
        generators = {
            "controlDict": ControlDictGenerator,
            "fvSchemes": FvSchemesGenerator,
            "fvSolution": FvSolutionGenerator,
            "transportProperties": TransportPropertiesGenerator,
        }
        for name, config in configs.items():
            gen = generators.get(name)
            if gen:
                p = gen.generate(self.case_path, **config)
                paths.append(p)
        return paths
