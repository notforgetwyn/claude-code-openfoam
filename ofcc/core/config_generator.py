"""
OpenFOAM 配置文件生成器。
将结构化参数转换为 OpenFOAM 字典格式文件。
"""

from pathlib import Path
from typing import Dict, Any, List
from ofcc.infra.logger import get_logger

logger = get_logger(__name__)


class OFDictWriter:
    """写入 OpenFOAM 字典格式文件"""

    @staticmethod
    def write(path: Path, entries: Dict[str, Any], header: str = None) -> None:
        """
        写入 OpenFOAM 字典文件。

        Args:
            path: 文件路径
            entries: 键值对字典
            header: 文件头注释
        """
        lines = []
        if header:
            lines.append(f"/* {header} */")
            lines.append(f"/* Generated by OFCC - {Path(__file__).parent.parent.parent.name} */")
            lines.append("")

        lines.append("FoamFile")
        lines.append("{")
        lines.append("    version     2.0;")
        lines.append("    format      ascii;")
        lines.append("    class       dictionary;")
        lines.append("    location    \"system\";")
        lines.append("    object      {name};".replace("{name}", path.stem))
        lines.append("}")
        lines.append("// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //")
        lines.append("")

        for key, value in entries.items():
            lines.append(OFDictWriter._format_entry(key, value, indent=0))

        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text("\n".join(lines) + "\n")
        logger.info(f"配置文件已写入: {path}")

    @staticmethod
    def _format_entry(key: str, value: Any, indent: int) -> str:
        """格式化单个条目"""
        pad = "    " * (indent + 1)
        if isinstance(value, dict):
            lines = [pad + key]
            lines.append(pad + "{")
            for k, v in value.items():
                lines.append(OFDictWriter._format_entry(k, v, indent + 1))
            lines.append(f"{pad}}}")
            return "\n".join(lines)
        elif isinstance(value, list):
            if all(isinstance(x, (int, float)) for x in value):
                formatted = "; ".join(str(v) for v in value)
                return f"{pad}{key}         {len(value)}*({formatted});"
            else:
                formatted = "; ".join(f'"{v}"' if isinstance(v, str) else str(v) for v in value)
                return f"{pad}{key}         {len(value)}({formatted});"
        elif isinstance(value, str):
            return f"{pad}{key}         \"{value}\";"
        elif isinstance(value, bool):
            return f"{pad}{key}         {'true' if value else 'false'};"
        else:
            return f"{pad}{key}         {value};"


class ControlDictGenerator:
    """生成 controlDict 文件"""

    SOLVERS = ["simpleFoam", "pisoFoam", "icoFoam", "snappyHexMesh", "blockMesh"]

    @classmethod
    def generate(cls, case_path: Path, solver: str, **kwargs) -> Path:
        """
        生成 controlDict 文件。

        Args:
            case_path: case 目录路径
            solver: 求解器名称
            **kwargs: startTime, endTime, deltaT, writeInterval, etc.
        """
        defaults = {
            "application": solver,
            "startFrom": "startTime",
            "startTime": 0,
            "stopAt": "endTime",
            "endTime": 1000,
            "deltaT": 1,
            "writeControl": "timeStep",
            "writeInterval": 100,
            "purgeWrite": 0,
            "writeFormat": "ascii",
            "writePrecision": 6,
            "writeCompression": "off",
            "timeFormat": "general",
            "timePrecision": 6,
            "runTimeModifiable": True,
        }
        defaults.update(kwargs)
        path = case_path / "system" / "controlDict"
        OFDictWriter.write(path, defaults, header="Control dictionary")
        return path


class FvSchemesGenerator:
    """生成 fvSchemes 文件"""

    @classmethod
    def generate(cls, case_path: Path, **kwargs) -> Path:
        defaults = {
            "ddtSchemes": {"default": "steadyState"},
            "gradSchemes": {"default": "Gauss linear"},
            "divSchemes": {"default": "Gauss linear"},
            "laplacianSchemes": {"default": "Gauss linear orthogonal"},
            "interpolationSchemes": {"default": "linear"},
            "snGradSchemes": {"default": "orthogonal"},
        }
        defaults.update(kwargs)
        path = case_path / "system" / "fvSchemes"
        OFDictWriter.write(path, defaults, header="Finite volume schemes")
        return path


